package dto;

public class user_live_location {
	private String userid;
	private String latitudce;
	private String longitude;

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getLatitudce() {
		return latitudce;
	}

	public void setLatitudce(String latitudce) {
		this.latitudce = latitudce;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

}
